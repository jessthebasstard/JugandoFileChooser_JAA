    private void muestraImagen(int propiedad){
         ImageIcon icono1 = new  ImageIcon(selectorArchivos.getSelectedFile().getPath());  
         img = icono1.getImage();         
         
        ImageIcon iconoEscalado = new ImageIcon(img.getScaledInstance(jLabelShowImage.getWidth(),
                    jLabelShowImage.getHeight(),propiedad));

        jLabelShowImage.setIcon(iconoEscalado);
    }
    
    
